// Get current language from cookie or default to 'en'
function getCurrentLang() {
    var match = document.cookie.match(new RegExp('(^| )django_language=([^;]+)'));
    if (match) {
        return match[2];
    }
    return 'de'; // Default to German as per original app
}

// Set language and save to cookie
function setLanguage(lang) {
    var d = new Date();
    d.setTime(d.getTime() + (365*24*60*60*1000)); // 1 year
    document.cookie = "django_language=" + lang + ";expires=" + d.toUTCString() + ";path=/";
    return lang;
}

// Switch language (toggle between EN and DE)
function switchLanguage() {
    var currentLang = getCurrentLang();
    var newLang = currentLang === 'en' ? 'de' : 'en';
    setLanguage(newLang);
    applyTranslations();
    updateLangLabels();
    
    // Reload page to apply Django template changes if needed
    // window.location.reload();
}

// Update the language labels in all toggle buttons
function updateLangLabels() {
    var currentLang = getCurrentLang();
    var labels = document.querySelectorAll('#langLabel, #langLabelMobile');
    labels.forEach(function(label) {
        if (label) {
            label.textContent = currentLang.toUpperCase();
        }
    });
}

// Apply translations to all elements with data-translate attribute
function applyTranslations() {
    var currentLang = getCurrentLang();
    var elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(function(el) {
        var key = el.getAttribute('data-translate');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.textContent = translations[currentLang][key];
        }
    });
    
    // Also handle placeholders
    var placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(function(el) {
        var key = el.getAttribute('data-translate-placeholder');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.placeholder = translations[currentLang][key];
        }
    });
    
    // Update language labels
    updateLangLabels();
}

// Initialize on page load
if (typeof document !== 'undefined') {
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', function() {
            applyTranslations();
            updateLangLabels();
        });
    } else {
        // DOM already loaded
        applyTranslations();
        updateLangLabels();
    }
}
